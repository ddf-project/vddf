require('babel-core/register');
require('babel-polyfill');
require('source-map-support/register');

function noop(module, file) {
  module._compile('', file);
}

[
  '.css', '.less', '.scss', '.styl',
  '.gif', '.jpg', '.png', '.svg',
  '.ttf', '.eot', '.woff', '.woff2'
].forEach(function(extension) {
  require.extensions[extension] = noop;
});

